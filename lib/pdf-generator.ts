import { jsPDF } from "jspdf"

interface ShipmentData {
  bookingRef: string
  sapRef: string
  origin: string
  destination: string
  carrier: string
  vessel: string
  sailing: string
  containerType: string
  plant: string
  mode: string
}

const DEFAULT_DATA: ShipmentData = {
  bookingRef: "MAEU-2024-SHA-78432",
  sapRef: "SAP-TM-87234",
  origin: "Shanghai (CNSHA)",
  destination: "Los Angeles (USLAX)",
  carrier: "Maersk Line",
  vessel: "AE-1234 (Ever Given)",
  sailing: "Mar 22, 2024",
  containerType: "2×40' High Cube",
  plant: "Suzhou Plant",
  mode: "Ocean FCL",
}

function addHeader(doc: jsPDF, title: string) {
  // Blue header bar
  doc.setFillColor(30, 58, 95)
  doc.rect(0, 0, 210, 25, "F")
  doc.setTextColor(255, 255, 255)
  doc.setFontSize(14)
  doc.setFont("helvetica", "bold")
  doc.text("Zero Touch Booking Agent", 15, 12)
  doc.setFontSize(9)
  doc.setFont("helvetica", "normal")
  doc.text("Automated Freight Documentation", 15, 18)

  // Document title
  doc.setTextColor(30, 58, 95)
  doc.setFontSize(16)
  doc.setFont("helvetica", "bold")
  doc.text(title, 15, 38)

  // Divider line
  doc.setDrawColor(200, 200, 200)
  doc.line(15, 42, 195, 42)
}

function addField(doc: jsPDF, label: string, value: string, x: number, y: number) {
  doc.setTextColor(120, 120, 120)
  doc.setFontSize(8)
  doc.setFont("helvetica", "normal")
  doc.text(label, x, y)
  doc.setTextColor(30, 30, 30)
  doc.setFontSize(10)
  doc.setFont("helvetica", "bold")
  doc.text(value, x, y + 5)
}

function addFooter(doc: jsPDF, data: ShipmentData) {
  const y = 270
  doc.setDrawColor(200, 200, 200)
  doc.line(15, y, 195, y)
  doc.setTextColor(150, 150, 150)
  doc.setFontSize(7)
  doc.setFont("helvetica", "normal")
  doc.text(`Booking Ref: ${data.bookingRef}  |  SAP Ref: ${data.sapRef}  |  Generated by Zero Touch Booking Agent`, 15, y + 5)
  doc.text(`Generated: ${new Date().toLocaleDateString("en", { year: "numeric", month: "long", day: "numeric" })} ${new Date().toLocaleTimeString("en", { hour: "2-digit", minute: "2-digit" })}`, 15, y + 9)
}

