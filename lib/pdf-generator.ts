export function generatePackingList(data: ShipmentData = DEFAULT_DATA): void {
  const doc = new jsPDF()
  addHeader(doc, "Commercial Packing List")

  let y = 50

  // Reference
  doc.setFillColor(245, 247, 250)
  doc.rect(15, y, 180, 20, "F")
  addField(doc, "Booking Reference", data.bookingRef, 20, y + 6)
  addField(doc, "Invoice No.", "INV-2024-SZ-04821", 100, y + 6)

  y += 28
  addField(doc, "Shipper", `${data.plant}`, 15, y)
  addField(doc, "Consignee", "Global Electronics Corp., Los Angeles", 100, y)

  y += 16

  // Items table
  doc.setTextColor(30, 58, 95)
  doc.setFontSize(11)
  doc.setFont("helvetica", "bold")
  doc.text("Packing Details", 15, y)
  y += 6

  // Table header
  doc.setFillColor(30, 58, 95)
  doc.rect(15, y, 180, 8, "F")
  doc.setTextColor(255, 255, 255)
  doc.setFontSize(8)
  doc.setFont("helvetica", "bold")
  const cols = [15, 30, 85, 115, 140, 165]
  doc.text("#", cols[0] + 2, y + 5.5)
  doc.text("Description", cols[1] + 2, y + 5.5)
  doc.text("Qty", cols[2] + 2, y + 5.5)
  doc.text("Net Wt (kg)", cols[3] + 2, y + 5.5)
  doc.text("Gross Wt (kg)", cols[4] + 2, y + 5.5)
  doc.text("Dims (cm)", cols[5] + 2, y + 5.5)

  y += 8

  const items = [
    ["1", "Laptop Motherboards (HS 8471.30)", "120 pcs", "4,200", "4,600", "60×40×30"],
    ["2", "LCD Display Panels (HS 8528.52)", "80 pcs", "3,400", "3,800", "55×35×25"],
    ["3", "SSD Storage Modules (HS 8471.70)", "200 pcs", "2,800", "3,000", "40×30×20"],
    ["4", "Battery Packs (HS 8507.60)", "160 pcs", "3,600", "3,900", "50×35×25"],
    ["5", "Keyboard Assemblies (HS 8471.60)", "120 pcs", "1,800", "2,000", "45×30×15"],
    ["6", "Power Adapters (HS 8504.40)", "240 pcs", "1,400", "1,700", "30×20×15"],
  ]

  items.forEach((item, idx) => {
    if (idx % 2 === 0) {
      doc.setFillColor(248, 249, 252)
      doc.rect(15, y, 180, 7, "F")
    }
    doc.setTextColor(50, 50, 50)
    doc.setFontSize(8)
    doc.setFont("helvetica", "normal")
    item.forEach((val, ci) => {
      doc.text(val, cols[ci] + 2, y + 5)
    })
    y += 7
  })

  // Totals
  y += 4
  doc.setDrawColor(30, 58, 95)
  doc.line(115, y, 195, y)
  y += 6
  doc.setFont("helvetica", "bold")
  doc.setFontSize(9)
  doc.setTextColor(30, 58, 95)
  doc.text("Total Packages:", 115, y)
  doc.text("240 cartons", 165, y)
  y += 6
  doc.text("Total Net Weight:", 115, y)
  doc.text("17,200 kg", 165, y)
  y += 6
  doc.text("Total Gross Weight:", 115, y)
  doc.text("18,400 kg", 165, y)

  y += 12
  addField(doc, "Container", data.containerType, 15, y)
  addField(doc, "Marks & Numbers", `MK: GEC-SZ-${data.bookingRef.slice(-5)}`, 100, y)

  addFooter(doc, data)
  const blob = doc.output("blob")
  window.open(URL.createObjectURL(blob), "_blank")
}

